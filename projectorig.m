% --- Executes on button press in pushbutton7.
function pushbutton7_Callback(hObject, eventdata, handles)
r1=str2num(get(handles.edit1,'string'));
r2=str2num(get(handles.edit2,'string'));
c=(str2num(get(handles.edit3,'string')))/1000000;
f=1.44/((r1+2*r2)*c);
t=0.693*(r1+(2*r2))*c;
t1=0.693*(r1+r2)*c;
t2=0.693*r1*c;
d=(t1/t)*100;
set(handles.text18,'string',num2str(t));
set(handles.text17,'string',num2str(d));
set(handles.text16,'string',num2str(f));

if f>100
    p=0:0.000001:0.02;
    out=2.5*square(2*pi*f*p,d)+2.5;
    figure(1);
    plot(p,out)
else
    p=0:0.0001:5;
    out=2.5*square(2*pi*f*p,d)+2.5;
    figure(1);
    plot(p,out);
end
ylim([0 6])
xlabel('Time(s)')
ylabel('Amplitude(v)')
title('waveform of 555 timer as an astable multivibrator')

    
    



